#INFORMAR SE OS TERMO a, b e c FORMAM UM TRIO PITAGÓRICO
a = int(input('Informe o valor A: '))
b = int(input('Informe o valor B: '))
c = int(input('Informe o valor C: '))
if a>=0 and b>=0 and c>=0:
    print('Os termos formam um Trio Pitagórico.')
else:
    print('Os termos não formam um Trio Pitagórico.')